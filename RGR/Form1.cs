        private void listView1_MouseClick(object sender, MouseEventArgs e)
        {
            if (e.Button == MouseButtons.Right)
            {
                int idx = listView1.SelectedItems[0].Index;
                string type = "000";
                string idd = spisok[idx].Id.ToString();
                string zna = spisok[listView1.SelectedItems[0].Index].Found ? "0" : "1";
                cart c = spisok[idx];
                c.Found = !c.Found;
                spisok[idx] = c;
                spisok[listView1.SelectedItems[0].Index].set(!spisok[listView1.SelectedItems[0].Index].Found);
                listView1.Invalidate();

                switch (spisok[listView1.SelectedItems[0].Index].Type)
                {
                    case "Рукопашный Отряд": type = "Melee Units"; break;
                    case "Дальнобойный Отряд": type = "Ranged Units"; break;
                    case "Осадный Отряд": type = "Siege Units"; break;
                    case "Специальная карта": type = "Special Cards"; break;
                    case "Лидер": type = "Leaders"; break;
                }
                string zap = "UPDATE '" + type + "' SET Found = " + zna + " WHERE id = " + idd + ";";

                SQLiteCommand command0 = new SQLiteCommand(zap, connection);
                connection.Open();
                command0.ExecuteNonQuery();
                connection.Close();
                listView1.Invalidate();
            }
        }
